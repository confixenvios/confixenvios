-- Limpar endereços de coleta (b2b_pickup_addresses) fora das faixas permitidas para Local
-- Faixas permitidas: 74000000-74899999, 74900000-74999999, 75000000-75159999, 
-- 75170000-75174999, 75250000-75264999, 75340000-75344999, 75345000-75349999, 75380000-75394799

DELETE FROM b2b_pickup_addresses
WHERE NOT (
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 74000000 AND 74899999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 74900000 AND 74999999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75000000 AND 75159999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75170000 AND 75174999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75250000 AND 75264999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75340000 AND 75344999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75345000 AND 75349999) OR
  (CAST(REPLACE(cep, '-', '') AS BIGINT) BETWEEN 75380000 AND 75394799)
);

